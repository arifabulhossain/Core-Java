package home_19_dec_18;

public class AssignmentOperatar {

    public static void main(String[] args) {
        int x = 3;
        int y = 2;
        x += y;    // x = x + y;  Result = 5
        System.out.println("The Sum is: " + x);
        
        
        x -= y;    // x = x - y; Result = 3
        System.out.println("The Sub is: " + x);
        
        x *= y;    // x = x * y; Result = 6
        System.out.println("The Multi is: " + x);
        
        
        x /= y;    // x = x / y; Result = 3
        System.out.println("The Div is: " + x);
        
        
        x %= y;    // x = x % y; Result = 01
        System.out.println("The mod is: " + x);
      
         
         
        
       
        

    }
}
